async def seed_default_config_and_prompts(db: AsyncSession):
    """Seed the database with default agent settings and prompts if empty."""
    # Check if configurations are already seeded
    config_check = await db.execute(select(AgentConfiguration))
    if not config_check.scalars().first():
        logger.info("Seeding default agent configurations...")
        for cfg in DEFAULT_CONFIGS:
            db_cfg = AgentConfiguration(**cfg)
            db.add(db_cfg)
        await db.flush()

    # Check if prompts are already seeded
    prompt_check = await db.execute(select(PromptTemplate))
    if not prompt_check.scalars().first():
        logger.info("Seeding default system prompt templates...")
        for prt in DEFAULT_PROMPTS:
            db_prt = PromptTemplate(**prt, updated_at=datetime.utcnow())
            db.add(db_prt)
        await db.flush()
    
    await db.commit()

async def get_agent_config(db: AsyncSession, agent_key: str) -> AgentConfiguration:
    """Fetch the configuration profile of a specific agent."""
    result = await db.execute(
        select(AgentConfiguration).where(AgentConfiguration.agent_key == agent_key)
    )
    cfg = result.scalar_one_or_none()
    if not cfg:
        # Fallback to local default lookup
        default_data = next((c for c in DEFAULT_CONFIGS if c["agent_key"] == agent_key), None)
        if default_data:
            cfg = AgentConfiguration(**default_data)
    return cfg

async def get_prompt_template(db: AsyncSession, agent_key: str) -> PromptTemplate:
    """Fetch the latest prompt template of a specific agent."""
    result = await db.execute(
        select(PromptTemplate)
        .where(PromptTemplate.agent_key == agent_key)
        .order_by(PromptTemplate.version.desc())
    )
    prompt = result.scalars().first()
    if not prompt:
        default_data = next((p for p in DEFAULT_PROMPTS if p["agent_key"] == agent_key), None)
        if default_data:
            prompt = PromptTemplate(**default_data, updated_at=datetime.utcnow())
    return prompt
